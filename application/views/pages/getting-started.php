<h1>Importing and deduplicating</h1>
<ol>
	<li>First export your library from Endnote selecting "show all fields" and saving the file as xml. (N.B. RIS and CSV imports do not currently work)</li>
	<li><a href="/signup">Create an account in the SRA</a> or <a href="/login">login if you already have one</a></li>
	<li>From the "tools" drop down menu select "deduplicate references"</li>
	<li>Select "Deduplicator" as the "tool to use"</li>
	<li>Choose "upload new library" as the "library to use"</li>
	<li>Browse to select your library</li>
	<li>Then select "process library"</li>
</ol>

<h1>Exporting deduplicated library</h1>
<ol>
	<li>Use the "Tools" drop down menu on the top right hand side of the screen.</li>
	<li>Select export references</li>
	<li>Choose "Endnote XML". (N.B. RIS and CSV have limited functionality at present)</li>
</ol>
